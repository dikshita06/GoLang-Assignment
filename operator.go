package main

import "fmt"

func main() {
	a := 40
	b := 6

	addition := a + b
	fmt.Println("Addition:", addition)

	substraction := a - b
	fmt.Println("Substraction:", substraction)

	multiplication := a * b
	fmt.Println("Multiplication:", multiplication)

	divison := a / b
	fmt.Println("Divison:", divison)

	remainder := a % b
	fmt.Println("Remainder:", remainder)

}